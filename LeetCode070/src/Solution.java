
/**
 * You are climbing a stair case. It takes n steps to reach to the top.
 * Each time you can either climb 1 or 2 steps. In how many distinct ways can you climb to the top?
 */
public class Solution {
	public int climbStairs(int n) {
		if (n < 3) {
			return n;
		}
		int sum1 = 1;
		int sum2 = 2;
		for (int i = 2; i < n; i ++) {
			sum2 = sum1 + sum2;
			sum1 = sum2 - sum1;
		}
		return sum2;
	}
	public static void main(String[] args) {
		Solution solution = new Solution();
		System.out.println(solution.climbStairs(0));
		System.out.println(solution.climbStairs(1));
		System.out.println(solution.climbStairs(2));
		System.out.println(solution.climbStairs(3));
		System.out.println(solution.climbStairs(4));
		System.out.println(solution.climbStairs(5));
		System.out.println(solution.climbStairs(6));
		System.out.println(solution.climbStairs(7));
		System.out.println(solution.climbStairs(8));
		System.out.println(solution.climbStairs(9));
		System.out.println(solution.climbStairs(10));
		System.out.println(solution.climbStairs(20));
		System.out.println(solution.climbStairs(50));
		System.out.println(solution.climbStairs(100));
	}
}
